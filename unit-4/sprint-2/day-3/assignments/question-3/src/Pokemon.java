public class Pokemon{

    public void even(){
        for(int i=1;i<21;i++){
            if(i%2==0){
                System.out.println("Even No. :"+i);
            }
        }
    }
    public void odd(){
        for (int i=1;i<21;i++){
            if(i%2!=0){
                System.out.println("Odd No. :"+i);
            }
        }
    }

}
